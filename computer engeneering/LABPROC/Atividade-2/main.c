void display()
{

  int tens = 0, unit = 0, aux = 2, estadoIN = 0, estadoOUT = 0, estadoIN2 = 0, estadoOUT2 = 0, outWait = 0, inWait = 0;
  while (1)
  {
    for (int delay = 0; delay < 25; delay++)
    {

      GPIOC->BSRR = (1 << 7) + (1 << 23);
      GPIOC->BSRR = (0 << 8) + (1 << 24);
      HAL_Delay(10);
      GPIOC->ODR = nums[tens];

      GPIOC->BSRR = (0 << 7) + (1 << 23);
      GPIOC->BSRR = (1 << 8) + (1 << 24);
      HAL_Delay(10);
      GPIOC->ODR = nums[unit];

      if (check_function() == 1)
        aux = 1;
      else if (check_function() == 0)
        aux = 0;
      else
        aux = 2;

      if (aux == 1 && outWait == 0 && inWait == 0)
      {
        estadoIN = 1;
        outWait = 1;
      }
      if (estadoIN == 1 && aux == 0 && inWait == 0)
      {
        estadoIN2 = 1;
        estadoIN = 0;
      }
      if (estadoIN2 == 1 && aux == 2)
      {
        if (unit < 9)
        {
          unit++;
        }
        else if (tens < 9)
        {

          tens++;
          unit = 0;
        }
        else
        {
          tens = 0;
          unit = 0;
        }
        estadoIN2 = 0;
        outWait = 0;
      }
      if (aux == 0 && outWait == 0 && inWait == 0)
      {
        estadoOUT = 1;
        inWait = 1;
      }
      if (estadoOUT == 1 && aux == 1 && outWait == 0)
      {
        estadoOUT2 = 1;
        estadoOUT = 0;
      }
      if (estadoOUT2 == 1 && aux == 2)
      {
        if (unit > 0)
        {
          unit--;
        }
        else if (tens > 0)
        {

          tens--;
          unit = 9;
        }
        else
        {
          tens = 0;
          unit = 0;
        }
        inWait = 0;
        estadoOUT2 = 0;
      }
      aux = 2;
    }
  }
}
